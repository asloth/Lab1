private void limpiarControles(){
        txtCodigo.setText("");
        txtNombre.setText("");
        txtDes.setText("");
        chkVigencia.setSelected(false);
        txtCodigo.requestFocus();
    }
    
    private void listarCategorias(){
        ResultSet rsMarcas = null;
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Codigo");
        model.addColumn("Nombre");
        model.addColumn("Descripcion");
        model.addColumn("Vigencia");
        try {
            rsMarcas = objMarca.listarCategoria();
            while (rsMarcas.next()){
                model.addRow(new Object[]{rsMarcas.getInt("codcategoria"), rsMarcas.getString("nomcategoria"),rsMarcas.getString("descripcion"), rsMarcas.getBoolean("vigencia")} );
            }
            tblCategorias.setModel(model);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error", "Error al listar tabla", WIDTH);
        }
        
    }

