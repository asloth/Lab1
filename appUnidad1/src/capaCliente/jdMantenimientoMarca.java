    private void btnNuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNuevoActionPerformed
        
        try {
            if (btnNuevo.getText().equals("Nuevo")) {
                txtCodigo.setText(String.valueOf(objMarca.generarCodigoMarca()));
                btnNuevo.setText("Guardar");
                txtNombre.requestFocus();
            }else{
                btnNuevo.setText("Nuevo");
                objMarca.registrar(Integer.parseInt(txtCodigo.getText()),txtNombre.getText() , chkVigencia.isSelected());
                limpiarControles();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,"Error");
        }
    }//GEN-LAST:event_btnNuevoActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        try {
            rs = objMarca.buscarMarca(Integer.parseInt(txtCodigo.getText()));
            if (rs.next()){
                txtNombre.setText(String.valueOf(rs.getString("nommarca")));
                chkVigencia.setSelected(rs.getBoolean("vigencia"));
            }else{
                JOptionPane.showMessageDialog(null,"No existe la marca");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,"No se encuentra la marca");
            limpiarControles();
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        try {
            if (txtCodigo.getText().isEmpty()){
                JOptionPane.showMessageDialog(null,"Debe ingresar un codigo");
            }else {
                objMarca.borrarMarca(Integer.parseInt(txtCodigo.getText()));
                limpiarControles();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,"Error eliminar");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

