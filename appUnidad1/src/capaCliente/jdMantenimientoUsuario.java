    private void listarUsuarios(){
        ResultSet rsUsuarios = null;
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Codigo");
        model.addColumn("Usuario");
        model.addColumn("Clave");
        model.addColumn("Nombre");
        model.addColumn("Cargo");
        model.addColumn("Vigencia");
        model.addColumn("Pregunta");
        model.addColumn("Respuesta");
        try {
            rsUsuarios = objUsuario.listarUsuario();
            while (rsUsuarios.next()){
                
                model.addRow(new Object[]{rsUsuarios.getInt("codusuario"), 
                    rsUsuarios.getString("nomusuario"),
                    rsUsuarios.getString("clave"), 
                    rsUsuarios.getString("nombrecompleto"),
                    rsUsuarios.getString("cargo"),
                    rsUsuarios.getBoolean("estado"), rsUsuarios.getString("pregunta"), 
                    rsUsuarios.getString("respuesta")} );
            }
            tblUsuarios.setModel(model);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al listar usuarios", "Error", WIDTH);
        }
    }
