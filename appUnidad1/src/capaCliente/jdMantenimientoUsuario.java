    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        try {
            objUsuario.modificarUsuario(Integer.parseInt(txtCodigo.getText()), txtUsuario.getText(), 
                    txtClave.getText(), txtNombre.getText(), txtCargo.getText(), txtPregunta.getText(), txtRespuesta.getText(), 
                    chkVigencia.isSelected());
            listarUsuarios();
            limpiarControles();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,"Error al modificar");
        }
    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnNuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNuevoActionPerformed

        try {
            if (btnNuevo.getText().equals("Nuevo")) {
                txtCodigo.setText(String.valueOf(objUsuario.generarCodigoUsuario()));
                btnNuevo.setText("Guardar");
                txtUsuario.requestFocus();
            }else{
                btnNuevo.setText("Nuevo");
                objUsuario.registrarUsuario(Integer.parseInt(txtCodigo.getText()),txtNombre.getText(), 
                        txtClave.getText(), txtNombre.getText(), txtCargo.getText(), txtPregunta.getText(), 
                        txtRespuesta.getText(), chkVigencia.isSelected());
                listarUsuarios();
                limpiarControles();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,"Error");
        }
    }//GEN-LAST:event_btnNuevoActionPerformed

