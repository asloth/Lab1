    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        try {
            if (txtCodigo.getText().isEmpty()){
                JOptionPane.showMessageDialog(null,"Debe ingresar un codigo");
            }else {
                objUsuario.borrarUsuario(Integer.parseInt(txtCodigo.getText()));
                listarUsuarios();
                limpiarControles();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,"Error eliminar");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        try {
            rs = objUsuario.buscarUsuario(Integer.parseInt(txtCodigo.getText()));
            if (rs.next()){
                txtUsuario.setText(String.valueOf(rs.getString("nomusuario")));
                txtClave.setText(rs.getString("clave"));
                txtCargo.setText(rs.getString("cargo"));
                txtNombre.setText(rs.getString("nombrecompleto"));
                txtPregunta.setText(rs.getString("pregunta"));
                txtRespuesta.setText(rs.getString("respuesta"));
                chkVigencia.setSelected(rs.getBoolean("estado"));
            }else{
                JOptionPane.showMessageDialog(null,"No existe el usuario");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,"No se encuentra el usuario");
            limpiarControles();
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

